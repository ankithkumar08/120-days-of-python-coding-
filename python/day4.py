#Number as Positive, Negative, or Zero

num = float(input("Enter a number: "))
if num > 0:
   print("Positive number")
elif num == 0:
   print("Zero")
else:
   print("Negative number")

#Number is Odd or Even

num = int(input("Enter a number: "))
if (num % 2) == 0:
   print(f"{num} is Even")
else:
   print(f"{0} is Odd")
